/**
 * Обработчик команды /help
 */
function registerHelpHandler(bot) {
  bot.help((ctx) => {
    ctx.reply(
      '*CytologyTrainer Bot — помощь*\n\n' +
      '*Команды:*\n' +
      '/start — приветствие и начало работы\n' +
      '/test — пройти тест по цитологии\n' +
      '/stats — твоя статистика и прогресс\n' +
      '/help — эта справка\n\n' +
      '*О платформе:*\n' +
      'Обучаем гинекологической цитологии по системе Bethesda и цитологии щитовидной железы. ' +
      'Русскоязычная аудитория, международные стандарты.\n\n' +
      '_По вопросам: напишите администратору платформы._',
      { parse_mode: 'Markdown' }
    );
  });
}

module.exports = { registerHelpHandler };
